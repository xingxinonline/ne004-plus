void rdft512(float *a, int isgn)
{
    void bitrv2(int n, int *ip, float *a);
    void cftfsub(int n, float *a, float *w);
    void cftbsub(int n, float *a, float *w);
    void rftfsub(int n, float *a, int nc, float *c);
    void rftbsub(int n, float *a, int nc, float *c);
    int nw, nc;
    float xi;

    nw = rdft512_ip[0];
    nc = rdft512_ip[1];

    if (isgn >= 0) {
        bitrv2(512, rdft512_ip + 2, a);
        cftfsub(512, a, rdft512_w);
        rftfsub(512, a, nc, rdft512_w + nw);

        xi = a[0] - a[1];
        a[0] += a[1];
        a[1] = xi;
    } else {
        a[1] = 0.5 * (a[0] - a[1]);
        a[0] -= a[1];

        rftbsub(512, a, nc, rdft512_w + nw);
        bitrv2(512, rdft512_ip + 2, a);
        cftbsub(512, a, rdft512_w);
    }
}

